class Contact:

    def __init__(self, first_name, last_name, nickname, organization, address, mobile, date, month, year, email):
        self.first_name = first_name
        self.last_name = last_name
        self.nickname = nickname
        self.organization = organization
        self.address = address
        self.mobile = mobile
        self.date = date
        self.month = month
        self.year = year
        self.email = email